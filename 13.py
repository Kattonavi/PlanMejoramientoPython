# operacion aritmetica ejercicio 1
# Solicitar al usuario los valores de a, b y c como números decimales
a = float(input("digite un numero para a "))
b = float(input("digite un numero para b "))
c = float(input("digite un numero para c "))

# Realizar la operación aritmética especificada en el ejercicio
resultado = (a**3 * (b**2 - 2*a*c))/(2*b)
 
# Imprimir el resultado obtenido
print(f"el resultado es {resultado}")
