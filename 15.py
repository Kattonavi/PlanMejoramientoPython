# intercambiar el valor de 2 variables ejercicio 3
# Solicitar al usuario los valores de a y b
a = input(" el valor de a es ")
b = input(" el valor de b es ")

# Intercambiar los valores de a y b utilizando una asignación múltiple
a , b = b , a

# Imprimir los nuevos valores de a y b
print(f"el nuevo valor de a es {a}")
print(f"el nuevo valor de a es {b}")