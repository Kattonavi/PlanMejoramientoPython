# operaciones de asignacion

a = 0
a += 5 # suma en asignacion
a -= 2 # resta en asignacion
a *= 3 # multiplicacion en asignacion
a /= 3 # divicion en asignacion
a **= 2 # potencial en asignacion
a %= 2 #modulo en asignacion

print(a)

# esto se usa para asignar operacion en una misma linea y asi ahorrarnos linea inecesarias de codigo